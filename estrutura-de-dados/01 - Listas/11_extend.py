linguagens = ["python", "js", "c"]

print(linguagens)  # ["python", "js", "c"]

linguagens.extend(["java", "csharp"])

print(linguagens)  # ["python", "js", "c", "java", "csharp"]

times = ["Palmeiras", "Corinthians", "São Paulo"]
print(times)  # ["Palmeiras", "Corinthians", "São Paulo"]
times.extend(["Santos", "Flamengo"])
print(times)  # ["Palmeiras", "Corinthians", "São Paulo", "Santos", "Flamengo"]
